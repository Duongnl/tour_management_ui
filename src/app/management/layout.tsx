import type { Metadata } from "next";
import { Inter } from "next/font/google";
import 'bootstrap/dist/css/bootstrap.min.css';
import AppHeader from "@/components/app_header";
import AppFooter from "@/components/app_footer";
import { getSessionId } from "@/utils/session_store";
import { Container } from "react-bootstrap";
import { redirect } from 'next/navigation'; // Import redirect
const inter = Inter({ subsets: ["latin"] });

export const dynamic = "force-dynamic";

export const metadata: Metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
};

export default async function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>,) {

  const res = await fetch(
    "http://localhost:8080/api/account/my-info",
    {
      method: "GET",
      headers: {
        Authorization: `Bearer ${getSessionId()}`, // Set Authorization header
      },
    }
  );
  if (!res.ok) {
    // Nếu phản hồi không thành công, chuyển hướng đến trang đăng nhập
    redirect('./');
    return null; // Đảm bảo rằng không có gì được render ra ngoài
  }

  const data = await res.json();
  const account = data.result;
  return (
    <>
      <AppHeader
      account= {account}
      />

      {children}

      <AppFooter />
    </>
  );
}
